import dotenv from 'dotenv';
dotenv.config();

import express from 'express';
import cors from 'cors'; // Certifique-se de que 'cors' estÃ¡ instalado
import jwt from 'jsonwebtoken';

import { register, login } from './controllers/authController.js'; 
import { generateCoursePreview, saveGeneratedCourse } from './controllers/courseController.js'; 
import { getCourseCategories, getCourseSubCategories, getCourseTagsByCategory } from './controllers/dataController.js'; 

const app = express();
const PORT = process.env.PORT || 3001;

// --- ConfiguraÃ§Ã£o CORS ---
const allowedOrigins = [
    'https://meuscursos.netlify.app', // Sua URL do frontend no Netlify
    'http://localhost:3000',          // Se vocÃª roda seu frontend localmente em 3000
    'http://localhost:5173',          // Ou outra porta que vocÃª usa em desenvolvimento local (ex: Vite usa 5173)
    'http://localhost:8080'           // Mais uma opÃ§Ã£o comum para desenvolvimento local
];

const corsOptions = {
    origin: function (origin, callback) {
        // Permite requisiÃ§Ãµes sem "origin" (como de apps mobile ou ferramentas como Postman)
        // OU se a origem estÃ¡ na lista de allowedOrigins
        if (!origin || allowedOrigins.indexOf(origin) !== -1) {
            callback(null, true);
        } else {
            callback(new Error('Not allowed by CORS'));
        }
    },
    methods: 'GET,HEAD,PUT,PATCH,POST,DELETE', // MÃ©todos HTTP que vocÃª permite
    credentials: true, // Importante se vocÃª usa cookies ou cabeÃ§alhos de autenticaÃ§Ã£o
    optionsSuccessStatus: 204 // Para algumas compatibilidades de navegadores antigos
};

app.use(cors(corsOptions)); // Aplica a configuraÃ§Ã£o CORS com as opÃ§Ãµes definidas
app.use(express.json()); 

// --- Middleware de AutenticaÃ§Ã£o (JWT Protection) ---
const protect = (req, res, next) => {
    let token;
    if (req.headers.authorization && req.headers.authorization.startsWith('Bearer')) {
        try {
            token = req.headers.authorization.split(' ')[1];
            const decoded = jwt.verify(token, process.env.JWT_SECRET);
            req.user = { 
                id: decoded.id, 
                isAdmin: decoded.isAdmin, 
                plan: decoded.plan 
            }; 
            next();
        } catch (error) {
            console.error('Erro na autenticaÃ§Ã£o do token:', error);
            return res.status(401).json({ message: 'NÃ£o autorizado, token invÃ¡lido ou expirado.' });
        }
    }
    if (!token) {
        return res.status(401).json({ message: 'NÃ£o autorizado, nenhum token fornecido.' });
    }
};

// --- Rotas da API ---

app.get('/', (req, res) => {
    res.send('Servidor do backend "Meus Cursos" estÃ¡ rodando! ðŸ™Œ');
});

// Rotas de AutenticaÃ§Ã£o
app.post('/api/auth/register', register); 
app.post('/api/auth/login', login);

// ATUALIZADO: Rota para gerar PRÃ‰-VISUALIZAÃ‡ÃƒO de Cursos (protegida)
app.post('/api/courses/generate-preview', protect, generateCoursePreview); 

// NOVA ROTA: Rota para SALVAR Cursos Gerados e Debitar CrÃ©ditos (protegida)
app.post('/api/courses/save-generated', protect, saveGeneratedCourse); 

// --- ROTAS PARA BUSCA DE DADOS ---
app.get('/api/data/categories', getCourseCategories);
app.get('/api/data/subcategories', getCourseSubCategories);
app.get('/api/data/tags/byCategory/:categoryId', getCourseTagsByCategory);

// --- Inicia o Servidor ---
app.listen(PORT, () => {
    console.log(`ðŸš€ Servidor do backend rodando em http://localhost:${PORT}`);
    console.log('Aguardando requisiÃ§Ãµes do frontend...');
    console.log('Endpoints disponÃ­veis:');
    console.log(`GET /`);
    console.log(`POST /api/auth/register`); 
    console.log(`POST /api/auth/login`);
    console.log(`POST /api/courses/generate-preview (protegida)`);
    console.log(`POST /api/courses/save-generated (protegida)`);
    console.log(`GET /api/data/categories`); 
    console.log(`GET /api/data/subcategories`);
    console.log(`GET /api/data/tags/byCategory/:categoryId`);
});