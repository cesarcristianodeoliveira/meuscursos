import dotenv from 'dotenv';
dotenv.config();

import express from 'express';
import cors from 'cors';
import jwt from 'jsonwebtoken'; 

// Importa as funÃ§Ãµes de registro e login do controlador de autenticaÃ§Ã£o
import { register, login } from './controllers/authController.js';

// --- Importa funÃ§Ãµes do dataController ---
// Agora importamos getTopCategories, createCategory, getSubcategories, createSubcategory, getTags, createTag e getPixabayImages
import {
    getTopCategories, 
    createCategory,
    getSubcategories, 
    createSubcategory,
    getTags, 
    createTag,
    getPixabayImages 
} from './controllers/dataController.js';

const app = express();
const PORT = process.env.PORT || 3001; 

// --- ConfiguraÃ§Ã£o CORS para ProduÃ§Ã£o ---
const allowedOrigins = [
    process.env.FRONTEND_URL, // Permite que a variÃ¡vel de ambiente seja usada
    'http://localhost:3000', // Para desenvolvimento local
    'https://meuscursos.netlify.app', // Se vocÃª estiver usando Netlify para o frontend
    'https://meuscursos.onrender.com', // Adicionado explicitamente a URL do seu frontend no Render
    // Adicione outras origens se necessÃ¡rio, por exemplo, se o seu backend estiver em um subdomÃ­nio diferente no Render
    // Ex: 'https://seubackend.onrender.com' se o frontend estiver em 'https://seubackend.onrender.com'
].filter(Boolean); // Remove entradas nulas/vazias se FRONTEND_URL nÃ£o estiver definida

app.use(cors({
    origin: function (origin, callback) {
        // Permite requisiÃ§Ãµes sem 'origin' (ex: mobile apps, curl)
        if (!origin) return callback(null, true);
        if (allowedOrigins.indexOf(origin) === -1) {
            const msg = `The CORS policy for this site does not allow access from the specified Origin: ${origin}`;
            console.error(msg); // Loga a origem que estÃ¡ sendo bloqueada
            return callback(new Error(msg), false);
        }
        return callback(null, true);
    },
    methods: 'GET,HEAD,PUT,PATCH,POST,DELETE',
    credentials: true,
    optionsSuccessStatus: 204,
}));

app.use(express.json()); 

// --- Middleware de AutenticaÃ§Ã£o (JWT Protection) ---
const protect = (req, res, next) => {
    let token;
    if (req.headers.authorization && req.headers.authorization.startsWith('Bearer')) {
        try {
            token = req.headers.authorization.split(' ')[1];
            const decoded = jwt.verify(token, process.env.JWT_SECRET);
            req.user = { 
                id: decoded.id, 
                isAdmin: decoded.isAdmin, 
                plan: decoded.plan 
            }; 
            next();
        } catch (error) {
            console.error('Erro na autenticaÃ§Ã£o do token:', error);
            return res.status(401).json({ message: 'NÃ£o autorizado, token invÃ¡lido ou expirado.' });
        }
    }
    if (!token) {
        return res.status(401).json({ message: 'NÃ£o autorizado, nenhum token fornecido.' });
    }
};

// --- Middleware para verificar se o usuÃ¡rio Ã© Admin ---
const adminProtect = (req, res, next) => {
    if (!req.user || !req.user.isAdmin) {
        return res.status(403).json({ message: 'Acesso negado. Apenas administradores podem realizar esta aÃ§Ã£o.' });
    }
    next();
};


// --- Rotas da API ---
app.get('/', (req, res) => {
    res.send('Servidor do backend "Meus Cursos" estÃ¡ rodando! ðŸ™Œ');
});

// Rotas de AutenticaÃ§Ã£o
app.post('/api/auth/register', register); 
app.post('/api/auth/login', login);

// --- ROTAS PARA CRIAÃ‡ÃƒO DE CURSOS (PROTEGIDAS) ---

// Rota para buscar as categorias (protegida)
app.get('/api/courses/create/top-categories', protect, getTopCategories); 

// Rota para criar uma nova categoria (protegida por adminProtect)
app.post('/api/categories', protect, adminProtect, createCategory); 

// Rota para buscar subcategorias (protegida)
app.get('/api/subcategories', protect, getSubcategories);

// Rota para criar uma nova subcategoria (protegida por adminProtect)
app.post('/api/subcategories', protect, adminProtect, createSubcategory);

// Rota para buscar tags (protegida)
app.get('/api/tags', protect, getTags);

// Rota para criar uma nova tag (protegida por adminProtect)
app.post('/api/tags', protect, adminProtect, createTag);

// Rota para buscar imagens do Pixabay (protegida)
app.get('/api/pixabay-images', protect, getPixabayImages);


// Tratamento de erros para JWT (se o token for invÃ¡lido, etc.)
app.use((err, req, res, next) => {
    if (err.name === 'UnauthorizedError') {
        return res.status(err.status).send({ message: err.message });
    }
    next(err);
});

// --- Inicia o Servidor ---
app.listen(PORT, () => {
    console.log(`ðŸš€ Servidor do backend rodando em http://localhost:${PORT}`);
    console.log('Aguardando requisiÃ§Ãµes do frontend...');
    console.log('Endpoints disponÃ­veis:');
    console.log(`GET /`);
    console.log(`POST /api/auth/register`); 
    console.log(`POST /api/auth/login`);
    console.log(`GET /api/courses/create/top-categories (protegida)`); 
    console.log(`POST /api/categories (protegida por admin)`);
    console.log(`GET /api/subcategories?categoryId=[id]&categoryName=[name] (protegida)`); 
    console.log(`POST /api/subcategories (protegida por admin)`);
    console.log(`GET /api/tags?categoryId=[id]&categoryName=[name]&subcategoryId=[id]&subcategoryName=[name] (protegida)`); 
    console.log(`POST /api/tags (protegida por admin)`);
    console.log(`GET /api/pixabay-images?searchQuery=[termo] (protegida)`); 
});
